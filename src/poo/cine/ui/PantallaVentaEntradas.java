    private void venderBtnClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_venderBtnClick
        Funcion funcion = (Funcion) selFuncion.getSelectedItem();
        Pelicula pelicula = (Pelicula) selPelicula.getSelectedItem();
        
        if (funcion == null || pelicula == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una pelicula y una funcion");
        } else {
            funcion.setPelicula(pelicula);
            boletero.venderEntrada(funcion);
        }
    }//GEN-LAST:event_venderBtnClick

    private void verEntradasBtnClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_verEntradasBtnClick
        DefaultListModel listModel = new DefaultListModel();
        
        Iterator<Entrada> it = entradas.iterator();
        while (it.hasNext()) {
            Entrada entrada = it.next();
            listModel.addElement(entrada.toString());
        }
        
        entradasList.setModel(listModel);
        
    }//GEN-LAST:event_verEntradasBtnClick

